public class IfElse {

    public static void main(String[] args) {

        /*
            IF Else statement
         */

        int nilai = 80;
        int absen = 90;

        if (nilai >= 75 && absen >= 75){
            System.out.println("selamat anda lulus");
        } else {
            System.out.println("silahkan coba lagi tahun depan");
        }
    }
}
