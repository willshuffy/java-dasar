public class TernaryOperator {

    public static void main(String[] args) {

        /***
         *  Ternary operator merupakan bentuk lain dari if else
         */

        var nilai = 75;
        String ucapan = nilai >= 75 ? "Selamat anda lulus" : "silahkan coba lagi";

        System.out.println(ucapan);
    }
}
